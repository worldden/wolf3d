#include "libft.h"

void	ft_putnbr(int n)
{
	if ((n >= 0) && (n < 10))
		ft_putchar(48 + n);
	if (n < 0)
	{
		ft_putchar('-');
		if (n == -2147483648)
		{
			ft_putchar('2');
			n = n % 1000000000;
		}
		ft_putnbr(n * (-1));
	}
	else if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
